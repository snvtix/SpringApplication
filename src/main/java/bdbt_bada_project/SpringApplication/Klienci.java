package bdbt_bada_project.SpringApplication;

public class Klienci {

    private int nr_klienta;
    private String imie;
    private String nazwisko;
    private String nr_telefonu;
    private String email;
    private int nr_rozglosni;

    public int getNr_klienta() {
        return nr_klienta;
    }
    public void setNr_klienta(int nr_klienta) {
        this.nr_klienta = nr_klienta;
    }
    public String getImie() {
        return imie;
    }
    public void setImie(String imie) {
        this.imie = imie;
    }
    public String getNazwisko() {
        return nazwisko;
    }
    public void setNazwisko(String nazwisko) {
        this.nazwisko = nazwisko;
    }
    public String getNr_telefonu() {
        return nr_telefonu;
    }
    public void setNr_telefonu(String nr_telefonu) {
        this.nr_telefonu = nr_telefonu;
    }
    public String getEmail() {
        return email;
    }
    public void setEmail(String email) {
        this.email = email;
    }
    public int getNr_rozglosni() {
        return nr_rozglosni;
    }
    public void setNr_rozglosni(int nr_rozglosni) {
        this.nr_rozglosni = nr_rozglosni;
    }

}
